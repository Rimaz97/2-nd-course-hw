// Создание массива из 10 случайных чисел
let array = [];
for (let i = 0; i < 10; i++) {
  array.push(Math.floor(Math.random() * 20));
}
// Нахождение минимального числа в массиве
let min = array[0];
for (let num of array) {
  if (num < min) {
    min = num;
  }
}
