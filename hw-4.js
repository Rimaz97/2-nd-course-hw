// Объявляем переменные для хранения номера первой пятницы месяца и текущей даты
let firstFriday = 0; // Создаем переменную, которая будет хранить номер первой пятницы месяца
let currentDate = new Date(); // Создаём объект текущей даты и времени
// Определяем первую пятницу месяца
while (firstFriday === 0) {
  currentDate.setDate(currentDate.getDate() + 1); // Переходим к следующему дню
  if (currentDate.getDay() === 5) { // Проверяем, является ли день пятницей
    firstFriday = currentDate.getDate(); // Если да, сохраняем номер дня в переменной firstFriday
  }
}
// Вывод
for (let i = 1; i <= 4; i++) {
  currentDate.setDate(firstFriday + i); // Устанавливаем текущую дату на следующую пятницу после первой
  console.log(`Сегодня пятница, ${currentDate.getDate()}-е число. Необходимо подготовить отчёт.`);
}

